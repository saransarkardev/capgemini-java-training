package assignments.Day5.CarHasTyre;

public class Tyre{

    private String brand;
    private int price;

    // Getters --> read the value
    public String getBrand() {
        return brand;
    }
    // Setters --> write the value
    public void setBrand(String brand) {
        this.brand = brand;
    }

    // Getters --> read the value
    public int getPrice() {
        return price;
    }
    // Setters --> write the value
    public void setPrice(int price) {
        this.price = price;
    }

}
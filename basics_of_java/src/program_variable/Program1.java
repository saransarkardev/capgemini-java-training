package program_variable;

public class Program1 {
//	In java default integer --> int
//	In java default decimal/point value --> double
	
	public static void main(String[] args) {
		
		// Variable block of memory data
		// datatype identifier;
		
		// byte --> 1 byte = 8 bits 
		// byte range -> Total 256 = (-128->-1)(0->127)
		byte a;
		a = 120;
		
		//	short --> 2 bytes = 16 bits
		short b;
		b = 1300;

		// int --> 4 bytes = 32 bits
		int c;
		c = 84932;
		
		//	long --> 8 bytes = 64 bits
		long d;
		d = 3492028418694L;
		//	we have to put L after the number explicitly otherwise we 
		//	will not be able to store long numbers.
		
//		Point values
//		In java default decimal/point value --> double
//		double --> 8 byte
//		float --> 4 byte
		float x = 3.2f;
		
//		Character
//		char --> 2 byte
		
//		Boolean
//		true/false --> 1 bit
		
//		Note : Default values will be given to the variables by JVM.
		
	}
}

package literals_data;

public class Program1 {
 
	public static void main(String[] args) {
//		literals Numbers --> integer,float 
		System.out.println("NUMBERS");
		System.out.println(100);
		System.out.println(-100);
		System.out.println(0.65);
		System.out.println(-6.7);
//		If the number is starting from 0 then java will convert it into it's octal format.
		System.out.println(001); // Output -> 1
		System.out.println(013); // Output -> 11
		
	}
}
